'''

19.	从键盘上输入两个不超过32767的整数，试编程序用竖式加法形式显示计算结果。
  例如:   输入   123,   85
  显示:   　123
　　+   　   85
  　　-------------
 　　　     208
'''
a, b = 123, 85
wid = 10  # 宽度
print(('%' + '%d' % wid + 'd') % a)
print(('+%' + '%d' % (wid - 1) + 'd') % b)
print('-' * wid)
print(('%' + '%d' % wid + 'd') % (a + b))
